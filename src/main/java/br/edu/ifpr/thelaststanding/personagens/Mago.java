package br.edu.ifpr.thelaststanding.personagens;

public class Mago extends Personagem{
    private int magia;
    private int poderSombrio;

    public int getMagia() {
        return magia;
    }

    public void setMagia(int magia) {
        this.magia = magia;
    }

    public int getPoderSombrio() {
        return poderSombrio;
    }

    public void setPoderSombrio(int poderSombrio) {
        this.poderSombrio = poderSombrio;
    }
}
